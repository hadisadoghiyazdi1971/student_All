from pathlib import Path
import argparse
import pandas as pd


def main():
    p = argparse.ArgumentParser()
    p.add_argument("--results-dir", required=True)
    p.add_argument("--out", default=None)
    args = p.parse_args()

    results = Path(args.results_dir)
    metrics = pd.read_csv(results / "metrics.csv")
    sens = pd.read_csv(results / "parameter_sensitivity.csv")

    out = Path(args.out) if args.out else results / "tables_for_paper.tex"

    with out.open("w", encoding="utf-8") as f:
        f.write("% Auto-generated LaTeX tables\n\n")

        f.write("\\begin{table}[htbp]\n\\centering\n")
        f.write("\\caption{Experimental comparison generated by the BLF code.}\\label{tab:generated_metrics}\n")
        f.write(metrics.to_latex(index=False, float_format=lambda x: f"{x:.4f}"))
        f.write("\\end{table}\n\n")

        f.write("\\begin{table}[htbp]\n\\centering\n")
        f.write("\\caption{Parameter sensitivity with respect to the number of hidden components.}\\label{tab:generated_sensitivity}\n")
        f.write(sens.to_latex(index=False, float_format=lambda x: f"{x:.4f}"))
        f.write("\\end{table}\n")

    print(f"Wrote {out}")


if __name__ == "__main__":
    main()
